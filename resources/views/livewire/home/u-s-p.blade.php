  <script>
    // Staggered reveal bij scroll
    const cards = document.querySelectorAll('#usp .reveal');
    const io = new IntersectionObserver((entries) => {
      entries.forEach((e) => {
        if (e.isIntersecting) {
          e.target.animate(
            [{ opacity: 0, transform: 'translateY(12px)' }, { opacity: 1, transform: 'translateY(0)' }],
            { duration: 450, delay: Math.min(120 * Number(e.target.dataset?.i || 0), 360),
              easing: 'cubic-bezier(.22,.61,.36,1)', fill: 'both' }
          );
          io.unobserve(e.target);
        }
      });
    }, { threshold: 0.18 });
    cards.forEach((c, i) => {
      c.dataset.i = i;
      c.style.viewTransitionName = `usp-card-${i}`;
      c.style.opacity = '0';
      io.observe(c);
    });
  </script>
